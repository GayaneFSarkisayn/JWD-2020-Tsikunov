package com.jwd.tsikunov.task3.model;

public class Square {
    public double countCircleSquare(double quadrateSquare) {
        return quadrateSquare / 4 * Math.PI;
    }
     public double countSquareDifference(double quadrateSquare, double circleSquare) {
        return quadrateSquare / circleSquare;
     }
}
